package com.prud.zm.pi.model;

public class EasyPayModel {
	private String destinationAccount;
	private String destinationSortCode;
	private String destAccountName;
	private String amount;
	private String sourceAccount;
	private String sourceSortCode;
	private String sourceAccountName;
	private String reference;
	private String remarks;
	private String phoneNumber;
	
	
	public String getDestinationAccount() {
		return destinationAccount;
	}
	public void setDestinationAccount(String destinationAccount) {
		this.destinationAccount = destinationAccount;
	}
	public String getDestinationSortCode() {
		return destinationSortCode;
	}
	public void setDestinationSortCode(String destinationSortCode) {
		this.destinationSortCode = destinationSortCode;
	}
	public String getDestAccountName() {
		return destAccountName;
	}
	public void setDestAccountName(String destAccountName) {
		this.destAccountName = destAccountName;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getSourceAccount() {
		return sourceAccount;
	}
	public void setSourceAccount(String sourceAccount) {
		this.sourceAccount = sourceAccount;
	}
	public String getSourceSortCode() {
		return sourceSortCode;
	}
	public void setSourceSortCode(String sourceSortCode) {
		this.sourceSortCode = sourceSortCode;
	}
	public String getSourceAccountName() {
		return sourceAccountName;
	}
	public void setSourceAccountName(String sourceAccountName) {
		this.sourceAccountName = sourceAccountName;
	}
	public String getReference() {
		return reference;
	}
	public void setReference(String reference) {
		this.reference = reference;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	
	
}
